#!/usr/bin/env python3
# Copyright 2026, Dario Casalinuovo. LGPL.
# Generates fs_caps_user.h (userspace C++) and fs_caps_kernel.h (kernel C)
# from fs_caps.json. Both headers embed FS_CAPS_CHECKSUM derived from the
# table contents; the kernel module logs it at module init so a userspace
# /kernel mismatch is detectable in dmesg.

import json
import sys
import os
import hashlib
import argparse


FLAG_PSEUDO      = 1 << 0
FLAG_PERSISTENT  = 1 << 1
FLAG_READONLY    = 1 << 2
FLAG_ATTR        = 1 << 3
FLAG_QUERY       = 1 << 4
FLAG_NODEMON     = 1 << 5


def load(path):
	with open(path) as fp:
		return json.load(fp)


def parse_magic(v):
	if isinstance(v, int):
		return v
	return int(v, 0)


def entry_flags(e):
	f = 0
	if e["pseudo"]:      f |= FLAG_PSEUDO
	if e["persistent"]:  f |= FLAG_PERSISTENT
	if e["readonly"]:    f |= FLAG_READONLY
	if e["attr"]:        f |= FLAG_ATTR
	if e["query"]:       f |= FLAG_QUERY
	if e["nodemon"]:     f |= FLAG_NODEMON
	return f


def checksum(entries):
	# Stable digest over normalised rows. Order-independent within rows;
	# order-dependent over the table (we keep file order canonical).
	h = hashlib.sha256()
	for e in entries:
		row = "%s|%08x|%02x|%s|%s\n" % (
			e["name"],
			parse_magic(e["magic"]) & 0xffffffff,
			entry_flags(e),
			e.get("legacy_name", ""),
			",".join(e.get("aliases", [])),
		)
		h.update(row.encode("utf-8"))
	return int(h.hexdigest()[:8], 16)


def emit_user(entries, ck, out):
	out.write("// Auto-generated by build/scripts/gen_fs_caps.py. Do not edit.\n")
	out.write("// Source: src/system/libroot2/fs/fs_caps.json\n\n")
	out.write("#ifndef _LIBROOT_FS_CAPS_USER_H\n")
	out.write("#define _LIBROOT_FS_CAPS_USER_H\n\n")
	out.write("#include <stdint.h>\n#include <string.h>\n#include <strings.h>\n\n")
	out.write("namespace BPrivate { namespace FsCaps {\n\n")
	out.write("static const uint32_t kChecksum = 0x%08xu;\n\n" % ck)
	out.write("enum {\n")
	out.write("\tPSEUDO      = 1u << 0,\n")
	out.write("\tPERSISTENT  = 1u << 1,\n")
	out.write("\tREADONLY    = 1u << 2,\n")
	out.write("\tATTR        = 1u << 3,\n")
	out.write("\tQUERY       = 1u << 4,\n")
	out.write("\tNODEMON     = 1u << 5\n")
	out.write("};\n\n")
	out.write("struct Entry {\n")
	out.write("\tconst char*\tname;\n")
	out.write("\tuint32_t\tmagic;\n")
	out.write("\tuint32_t\tflags;\n")
	out.write("\tconst char*\tlegacy_name;\n")
	out.write("};\n\n")
	out.write("struct Alias {\n")
	out.write("\tconst char*\talias;\n")
	out.write("\tconst char*\tname;\n")
	out.write("};\n\n")
	out.write("static const Entry kEntries[] = {\n")
	for e in entries:
		out.write("\t{ \"%s\", 0x%08xu, 0x%02xu, \"%s\" },\n" % (
			e["name"], parse_magic(e["magic"]) & 0xffffffff,
			entry_flags(e), e.get("legacy_name", "")))
	out.write("};\n")
	out.write("static const unsigned int kEntryCount = sizeof(kEntries)/sizeof(kEntries[0]);\n\n")
	out.write("static const Alias kAliases[] = {\n")
	for e in entries:
		for a in e.get("aliases", []):
			out.write("\t{ \"%s\", \"%s\" },\n" % (a, e["name"]))
	out.write("\t{ 0, 0 }\n};\n\n")
	out.write("""static inline const Entry*
by_name(const char* mntType)
{
	if (mntType == 0)
		return 0;
	for (unsigned int i = 0; i < kEntryCount; i++) {
		if (strcasecmp(mntType, kEntries[i].name) == 0)
			return &kEntries[i];
	}
	for (unsigned int i = 0; kAliases[i].alias != 0; i++) {
		if (strcasecmp(mntType, kAliases[i].alias) == 0) {
			for (unsigned int j = 0; j < kEntryCount; j++) {
				if (strcmp(kEntries[j].name, kAliases[i].name) == 0)
					return &kEntries[j];
			}
		}
	}
	return 0;
}

static inline const Entry*
by_magic(uint32_t fType)
{
	for (unsigned int i = 0; i < kEntryCount; i++) {
		if (kEntries[i].magic == fType)
			return &kEntries[i];
	}
	return 0;
}

static inline bool is_pseudo(const char* mntType) {
	const Entry* e = by_name(mntType);
	return e != 0 && (e->flags & PSEUDO);
}
static inline bool is_persistent(const char* mntType) {
	const Entry* e = by_name(mntType);
	return e != 0 && (e->flags & PERSISTENT);
}
static inline bool is_readonly(const char* mntType) {
	const Entry* e = by_name(mntType);
	return e != 0 && (e->flags & READONLY);
}
static inline bool supports_node_monitor(uint32_t fType) {
	const Entry* e = by_magic(fType);
	return e != 0 && (e->flags & NODEMON);
}
static inline const char* legacy_name(const char* mntType) {
	const Entry* e = by_name(mntType);
	if (e == 0 || e->legacy_name == 0 || e->legacy_name[0] == 0)
		return mntType;
	return e->legacy_name;
}

} }
""")
	out.write("\n#endif\n")


def emit_kernel(entries, ck, out):
	out.write("/* Auto-generated by build/scripts/gen_fs_caps.py. Do not edit. */\n")
	out.write("/* Source: src/system/libroot2/fs/fs_caps.json */\n\n")
	out.write("#ifndef _NEXUS_FS_CAPS_KERNEL_H\n")
	out.write("#define _NEXUS_FS_CAPS_KERNEL_H\n\n")
	out.write("#include <linux/types.h>\n#include <linux/string.h>\n\n")
	out.write("#define FS_CAPS_CHECKSUM 0x%08xu\n\n" % ck)
	out.write("#define FS_CAP_PSEUDO     (1u << 0)\n")
	out.write("#define FS_CAP_PERSISTENT (1u << 1)\n")
	out.write("#define FS_CAP_READONLY   (1u << 2)\n")
	out.write("#define FS_CAP_ATTR       (1u << 3)\n")
	out.write("#define FS_CAP_QUERY      (1u << 4)\n")
	out.write("#define FS_CAP_NODEMON    (1u << 5)\n\n")
	out.write("struct fs_cap_entry {\n")
	out.write("\tconst char\t*name;\n")
	out.write("\tu32\t\tmagic;\n")
	out.write("\tu32\t\tflags;\n")
	out.write("};\n\n")
	out.write("static const struct fs_cap_entry fs_caps_entries[] = {\n")
	for e in entries:
		out.write("\t{ \"%s\", 0x%08xu, 0x%02xu },\n" % (
			e["name"], parse_magic(e["magic"]) & 0xffffffff, entry_flags(e)))
	out.write("};\n\n")
	out.write("static const unsigned int fs_caps_entry_count =\n")
	out.write("\tsizeof(fs_caps_entries)/sizeof(fs_caps_entries[0]);\n\n")
	out.write("""static inline const struct fs_cap_entry *
fs_caps_kernel_by_name(const char *n)
{
	unsigned int i;
	if (!n)
		return NULL;
	for (i = 0; i < fs_caps_entry_count; i++) {
		if (!strcmp(n, fs_caps_entries[i].name))
			return &fs_caps_entries[i];
	}
	return NULL;
}

static inline bool fs_caps_kernel_is_pseudo(const char *n)
{
	const struct fs_cap_entry *e = fs_caps_kernel_by_name(n);
	return e && (e->flags & FS_CAP_PSEUDO);
}

static inline bool fs_caps_kernel_is_readonly(const char *n)
{
	const struct fs_cap_entry *e = fs_caps_kernel_by_name(n);
	return e && (e->flags & FS_CAP_READONLY);
}

static inline u32 fs_caps_kernel_caps_for(const char *n)
{
	const struct fs_cap_entry *e = fs_caps_kernel_by_name(n);
	return e ? e->flags : 0;
}

""")
	out.write("#endif\n")


def main():
	ap = argparse.ArgumentParser()
	ap.add_argument("--json", required=True)
	ap.add_argument("--user", required=True)
	ap.add_argument("--kernel", required=True)
	args = ap.parse_args()

	data = load(args.json)
	entries = data["entries"]
	ck = checksum(entries)

	with open(args.user, "w") as f:
		emit_user(entries, ck, f)
	with open(args.kernel, "w") as f:
		emit_kernel(entries, ck, f)

	sys.stderr.write("fs_caps: %d entries, checksum=0x%08x\n" %
		(len(entries), ck))


if __name__ == "__main__":
	main()
